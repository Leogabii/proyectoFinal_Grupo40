private void armarCabecera() {
        modelo.addColumn("Pedido");
        modelo.addColumn("Fecha");
        modelo.addColumn("Importe");
        modelo.addColumn("Cobrado");
        jtPedidos.setModel(modelo);
    }
    
    private void cargarCombo1() {
        MeseroData ms = new MeseroData();
        List<Mesero> meseros = new ArrayList<>();
        meseros = ms.listaDeMeseros();
        for (Mesero mesero: meseros) {
            jcbMesero.addItem(mesero);
        }

    }
    
    private void cargarCombo2() {
        Mesero mesero = new Mesero();
        mesero = (Mesero) jcbMesero.getSelectedItem();
        PedidoData pd = new PedidoData();
    List<Mesa> lista = new ArrayList<>();
    lista = pd.listarMesasPorIdMesero(mesero.getIdMesero());
    for(Mesa mesa: lista) {
        jcbMesa.addItem(mesa);
    }
    }

