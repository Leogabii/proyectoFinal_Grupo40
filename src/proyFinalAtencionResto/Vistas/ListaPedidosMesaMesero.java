    private void jbCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCargarActionPerformed
        // TODO add your handling code here:
        PedidoData pd = new PedidoData();
        List<Pedido> pedidos = new ArrayList<>();
        Mesero mesero = (Mesero)jcbMesero.getSelectedItem();      
        Mesa mesa = (Mesa) jcbMesa.getSelectedItem();
        int nroMesero = mesero.getIdMesero();
        int nroMesa = mesa.getIdMesa();
        pedidos = pd.listarPedidosPorIdMeseroIdMesa(nroMesero, nroMesa);
        for (Pedido pedido: pedidos) {
            modelo.addRow(new Object[]{pedido.getIdPedido(), pedido.getFecha_hora(), pedido.getImporte(), pedido.isCobrado()});
        }
        jbCargar.setEnabled(false);
    }//GEN-LAST:event_jbCargarActionPerformed

